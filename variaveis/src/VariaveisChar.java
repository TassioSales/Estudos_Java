public class VariaveisChar {
    public static void main(String[] args){

        char o = 'o';
        char i = 'i';

        char a = 111;
        char b = 105;

        char interrogacao = 0X00E1;

        System.out.println(o+i);
        System.out.println("" + o + i);
        System.out.println("" + a + b);
        System.out.println(interrogacao);
    }
}
